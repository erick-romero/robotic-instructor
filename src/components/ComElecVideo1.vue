¿Qué es la electrónica?
Tecnología: Una palabra muy común hoy en día, ¿Quien no disfruta de la misma?, su televisión, la radio, incluso aquel divertido juguete de baterías. Esto funciona gracias a la electrónica: ¿Han oído hablar de ella? 
La electrónica es el campo de la ingeniería y la física que se encarga de diseñar los circuitos eléctricos capaces de hacer cosas tan maravillosas, es la rama de la ciencia que se ocupa del estudio del flujo y control de electrones y del estudio de sus comportamientos y efectos en aspiradoras gases y semiconductores y con dispositivos que utilizan dichos electrones.
