Usos generales:
La electrónica desarrolla en la actualidad una gran variedad de tareas, los principales usos de los circuitos eléctricos son:
El control, el procesado, la distribución de información, la conversión y la distribución de la energía eléctrica. 
Estos usos implican la creación o la detección de campos electromagnéticos y corrientes eléctricas, entonces, se puede decir que la electrónica abarca en general las siguientes áreas de aplicación:
